/**
 * Generate public/components/cookbook.html from e2e/fixtures/cookbook/*
 * (improvements.md I4b). The fixture IS the doc: every page shown here is
 * the exact source scripts/cookbook-check.mjs runs in CI — there is no
 * second copy to rot. Runs before website dev/build (package.json scripts),
 * like gen-stats.js; the output is gitignored.
 */
import { readdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..', '..');
const BASE = join(ROOT, 'e2e/fixtures/cookbook');
const OUT = join(ROOT, 'website/public/components/cookbook.html');

const esc = (s) => s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
// The files worth showing, in display order — first one that exists leads.
const SHOW = ['page.html', 'pages/index.html', 'spark.json', 'spark.config.js', 'main.js', 'setup.js', 'seed/todos.json', 'seed/users.json', 'counter.test.js', 'Dockerfile'];

const recipes = readdirSync(BASE).sort().map((name) => {
  const dir = join(BASE, name);
  const meta = JSON.parse(readFileSync(join(dir, 'recipe.json'), 'utf8'));
  const files = SHOW.filter((f) => existsSync(join(dir, f)))
    .map((f) => ({ name: f, src: readFileSync(join(dir, f), 'utf8').trimEnd() }));
  return { name, ...meta, files };
});

const aside = recipes.map((r) => `    <a href="#${r.name}">${esc(r.title)}</a>`).join('\n');
const body = recipes.map((r) => `    <h2 id="${r.name}">${esc(r.title)}</h2>
    <p class="modes">${r.modes.map((m) => `<code>${m}</code>`).join(' ')}</p>
    <p>${esc(r.description)}</p>
${r.files.map((f) => `    <p class="filename"><code>${esc(f.name)}</code></p>
    <pre spark-ignore>${esc(f.src)}</pre>`).join('\n')}`).join('\n\n');

writeFileSync(OUT, `<!-- GENERATED by website/scripts/gen-cookbook.mjs from e2e/fixtures/cookbook/
     — edit the fixtures, not this file. Every page below runs in CI
     (scripts/cookbook-check.mjs). -->
<div class="docs" spark-ignore>
  <aside>
    <p class="aside-title">Cookbook</p>
${aside}
  </aside>

  <main>
    <h1>Cookbook</h1>
    <p class="intro">Task-shaped recipes — each one a complete, runnable page (the exact sources below run in CI on every commit). The <code>modes</code> line says where it applies: client, SSR, prerender.</p>

${body}
  </main>
</div>
`);
console.log(`[gen-cookbook] ${recipes.length} recipes → components/cookbook.html`);
